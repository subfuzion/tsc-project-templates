/**
 * Returns sum of a and b.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function add(a, b) {
  return a + b;
}

/**
 * Returns sum of a and b.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function sub(a, b) {
  return a - b;
}

/**
 * Returns the product of a times b.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function mul(a, b) {
  return a * b;
}

/**
 * Returns the quotient of a divided by b.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function div(a, b) {
  return a / b;
}